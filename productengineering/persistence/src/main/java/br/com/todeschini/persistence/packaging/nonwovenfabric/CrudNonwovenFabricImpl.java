package br.com.todeschini.persistence.packaging.nonwovenfabric;

import br.com.todeschini.domain.business.packaging.nonwovenfabric.DNonwovenFabric;
import br.com.todeschini.domain.business.packaging.nonwovenfabric.spi.CrudNonwovenFabric;
import br.com.todeschini.domain.exceptions.DuplicatedResourceException;
import br.com.todeschini.domain.exceptions.ResourceNotFoundException;
import br.com.todeschini.persistence.entities.enums.Status;
import br.com.todeschini.persistence.entities.packaging.NonwovenFabric;
import br.com.todeschini.persistence.util.EntityService;
import org.springframework.stereotype.Component;
import org.springframework.transaction.annotation.Transactional;

import java.util.Collection;
import java.util.List;

@Component
public class CrudNonwovenFabricImpl implements CrudNonwovenFabric {

    private final NonwovenFabricRepository repository;
    private final NonwovenFabricDomainToEntityAdapter adapter;
    private final EntityService entityService;

    public CrudNonwovenFabricImpl(NonwovenFabricRepository repository, NonwovenFabricDomainToEntityAdapter adapter, EntityService entityService) {
        this.repository = repository;
        this.adapter = adapter;
        this.entityService = entityService;
    }

    @Override
    @Transactional(readOnly = true)
    public Collection<? extends DNonwovenFabric> findByDescription(String description) {
        return repository.findByDescription(description).stream().map(adapter::toDomain).toList();
    }

    @Override
    @Transactional(readOnly = true)
    public List<DNonwovenFabric> findAllActiveAndCurrentOne(Long obj) {
        return repository.findAllActiveAndCurrentOne(obj).stream().map(adapter::toDomain).toList();
    }

    @Override
    @Transactional(readOnly = true)
    public DNonwovenFabric find(Long id) {
        return adapter.toDomain(repository.findById(id).orElseThrow(() -> new ResourceNotFoundException("Código não encontrado: " + id)));
    }

    @Override
    @Transactional
    public DNonwovenFabric insert(DNonwovenFabric obj) {
        if(repository.existsById(obj.getCode())){
            throw new DuplicatedResourceException("Código já existente: " + obj.getCode());
        }
        entityService.verifyDependenciesStatus(adapter.toEntity(obj));
        return adapter.toDomain(repository.save(adapter.toEntity(obj)));
    }

    @Override
    @Transactional
    public DNonwovenFabric update(Long id, DNonwovenFabric obj) {
        if(!repository.existsById(obj.getCode())){
            throw new ResourceNotFoundException("Código não encontrado: " + obj.getCode());
        }
        NonwovenFabric entity = adapter.toEntity(obj);
        entityService.verifyDependenciesStatus(entity);
        setCreationProperties(entity);
        repository.save(entity);
        return adapter.toDomain(entity);
    }

    @Override
    @Transactional
    public void inactivate(Long id) {
        NonwovenFabric entity = repository.findById(id).orElseThrow(() -> new ResourceNotFoundException("Código não encontrado: " + id));
        Status newStatus = entity.getStatus() == Status.ACTIVE ? Status.INACTIVE : Status.ACTIVE;
        entity.setStatus(newStatus);
        repository.save(entity);
    }

    @Override
    @Transactional
    public void delete(Long id) {
        entityService.changeStatusToOther(repository.findById(id).orElseThrow(() -> new ResourceNotFoundException("Código não encontrado: " + id)), Status.DELETED);
    }

    private void setCreationProperties(NonwovenFabric obj){
        obj.setCreationDate(repository.findCreationDate(obj.getCode()));
        obj.setCreatedBy(repository.findCreatedBy(obj.getCode()));
    }
}
